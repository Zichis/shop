    public function confirm(Order $order)
    {
        $order->update([
            'status' => 'complete'
        ]);

        return redirect()->route('admin.orders.index');
    }

    public function reject(Order $order)
    {
        $order->update([
            'status' => 'pending'
        ]);

        toast('You successfully cancelled order!','success')->width('20rem')->position('top');

        return redirect()->route('admin.orders.index');
    }
